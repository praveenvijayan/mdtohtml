#!/usr/bin/env node
// pr-size-check.mjs — enforce the agent PR size limit mechanically.
//
// AGENTS.md step 3 caps an issue's scope at ~400 changed lines / ~6 files and
// tells the agent to split-and-requeue when it exceeds that. On its own that
// limit is honor-system only, and oversized PRs are the biggest drain on the
// loop's bottleneck resource — human review. This script turns the cap into a
// machine check: the `pr-gates` workflow runs it on every `agent/issue-*` PR
// and a red check names the overflow, quotes the numbers, and repeats the
// split-and-requeue protocol so the agent knows exactly what to do next.
//
// Thresholds live in GATES.md (criterion 3) so they are tuned as project
// config, never hard-coded here; absent config falls back to the manual's
// ~400 lines / ~6 files. PR size comes from the pull_request event payload,
// passed in as env vars so this stays a pure, testable function of its inputs.
//
// Zero dependencies. Requires Node 20+.
//   Run:  PR_ADDITIONS=.. PR_DELETIONS=.. PR_CHANGED_FILES=.. node scripts/pr-size-check.mjs
//         PR_FILES_JSON='[{"filename":"src/a.js","additions":1,"deletions":0}]' node scripts/pr-size-check.mjs
//   Override the config file for testing with GATES_FILE=/path/to/GATES.md.

import { existsSync, readFileSync, appendFileSync } from "node:fs";
import { ghClient, paginate } from "./gh-api.mjs";

const GATES_FILE = process.env.GATES_FILE || "GATES.md";
// The pr-gates workflow extracts the base branch's GATES.md and points
// BASE_GATES_FILE at it, so a PR is judged by size thresholds / exclusions it
// cannot edit in the same diff (issue #84 — a PR could otherwise raise its own
// max_changed_lines or add an exclude_paths covering its files and go green).
// When set, that base copy is the authoritative config; unset (local runs,
// tests), the working-tree GATES.md is used exactly as before.
const BASE_GATES_FILE = process.env.BASE_GATES_FILE || "";
const CONFIG_FILE = BASE_GATES_FILE || GATES_FILE;
const DEFAULT_MAX_LINES = 400; // AGENTS.md step 3
const DEFAULT_MAX_FILES = 6; //   ~400 changed lines / ~6 files
const DEFAULT_EXCLUDE_PATTERNS = [
  "package-lock.json",
  "pnpm-lock.yaml",
  "yarn.lock",
  "Cargo.lock",
  "poetry.lock",
  "go.sum",
  // Generated skill mirrors. Skills have one canonical source under
  // .agents/skills/ (which still counts) and two mirrors regenerated by
  // setup.sh, so one real skill edit ships three changed files. Excluding the
  // mirrors keeps the gate counting the single canonical change. Baked in like
  // the lockfiles above so a PR cannot lower its own bar (#84).
  ".claude/skills/**",
  "plugin/skills/**",
];

// Best-effort line into the Actions check summary; decorative, never the
// signal (that is the exit code), so a summary hiccup must not mask a result.
function summary(line) {
  const f = process.env.GITHUB_STEP_SUMMARY;
  if (f) {
    try {
      appendFileSync(f, line + "\n");
    } catch {
      /* summary is decorative; the exit code is the real signal */
    }
  }
}
const notice = (msg) => console.log(`::notice::${msg}`);
const errorAnnot = (msg) => console.log(`::error::${msg}`);

// --- read size config from GATES.md -----------------------------------------
// Parse `max_changed_lines:` / `max_changed_files:` anywhere in GATES.md. A
// missing file or key falls back to the manual's defaults rather than failing
// — the limit must still be enforced even before a project tunes it. Optional
// `exclude_paths:` entries add glob-ish path patterns to the default lockfile
// exclusions.
function parseList(value) {
  const raw = value.trim();
  if (!raw) return [];
  const body = raw.startsWith("[") && raw.endsWith("]") ? raw.slice(1, -1) : raw;
  return body
    .split(",")
    .map((s) => s.trim().replace(/^["']|["']$/g, ""))
    .filter(Boolean);
}

function readConfig(text) {
  const num = (key, fallback) => {
    const m = text.match(new RegExp(`max_changed_${key}\\s*[:=]\\s*(\\d+)`, "i"));
    return m ? Number(m[1]) : fallback;
  };

  const configuredExcludes = [];
  const lines = text.split("\n");
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const inline = line.match(/^\s*-?\s*(?:size_)?exclude_paths\s*[:=]\s*(.*?)\s*$/i);
    if (!inline) continue;

    configuredExcludes.push(...parseList(inline[1]));
    if (inline[1].trim()) continue;

    for (let j = i + 1; j < lines.length; j++) {
      const item = lines[j].match(/^\s+-\s+(.+?)\s*$/);
      if (!item) break;
      configuredExcludes.push(...parseList(item[1]));
    }
  }

  return {
    maxLines: num("lines", DEFAULT_MAX_LINES),
    maxFiles: num("files", DEFAULT_MAX_FILES),
    excludePatterns: [...new Set([...DEFAULT_EXCLUDE_PATTERNS, ...configuredExcludes])],
  };
}

const gatesText = existsSync(CONFIG_FILE) ? readFileSync(CONFIG_FILE, "utf8") : "";
const { maxLines, maxFiles, excludePatterns } = readConfig(gatesText);

// Criterion 2 (#84): when thresholds/exclusions came from a base copy, a PR that
// also edits its own GATES.md must be flagged — the change is deferred to the
// reviewer and takes effect only after merge, never lowering the bar for the PR
// that introduced it.
if (BASE_GATES_FILE) {
  let headText = "";
  try {
    headText = existsSync(GATES_FILE) ? readFileSync(GATES_FILE, "utf8") : "";
  } catch {
    headText = "";
  }
  if (headText !== gatesText) {
    const msg = `This PR modifies ${GATES_FILE}. Size thresholds and exclusions were read from the base branch's config, so the change only takes effect after merge — review the ${GATES_FILE} diff.`;
    console.log(`::warning::${msg}`);
    summary(`### PR size\n\n> ⚠️ ${msg}\n`);
  }
}

// --- read the PR's size from the event payload ------------------------------
// Missing or non-numeric counts mean the workflow wired the check up wrong;
// fail loudly (Hard Rule 8) rather than passing a PR whose size is unknown.
function intFromEnv(name) {
  const raw = process.env[name];
  if (raw === undefined || raw === "" || !/^\d+$/.test(raw.trim())) return null;
  return Number(raw.trim());
}

const additions = intFromEnv("PR_ADDITIONS");
const deletions = intFromEnv("PR_DELETIONS");
const changedFiles = intFromEnv("PR_CHANGED_FILES");

function globToRegExp(pattern) {
  const escaped = pattern
    .replace(/[|\\{}()[\]^$+?.]/g, "\\$&")
    .replace(/\*\*/g, "\0")
    .replace(/\*/g, "[^/]*")
    .replace(/\0/g, ".*");
  return new RegExp(`^${escaped}$`);
}

function matchesPattern(path, pattern) {
  const normalizedPath = path.replace(/\\/g, "/");
  const normalizedPattern = pattern.replace(/\\/g, "/").replace(/^\/+/, "");
  if (!normalizedPattern.includes("/") && !normalizedPattern.includes("*")) {
    return normalizedPath === normalizedPattern || normalizedPath.endsWith(`/${normalizedPattern}`);
  }
  return globToRegExp(normalizedPattern).test(normalizedPath);
}

function excludedBy(path) {
  return excludePatterns.find((pattern) => matchesPattern(path, pattern)) || null;
}

function parseFileEntry(file) {
  const filename = file.filename || file.path || file.name;
  const fileAdditions = Number(file.additions);
  const fileDeletions = Number(file.deletions);
  if (!filename || !Number.isFinite(fileAdditions) || !Number.isFinite(fileDeletions)) return null;
  return { filename, additions: fileAdditions, deletions: fileDeletions };
}

function parseFilesJson(raw) {
  let parsed;
  try {
    parsed = JSON.parse(raw);
  } catch {
    throw new Error("PR_FILES_JSON was not valid JSON.");
  }
  if (!Array.isArray(parsed)) throw new Error("PR_FILES_JSON must be a JSON array.");
  const files = parsed.map(parseFileEntry);
  if (files.some((file) => !file)) {
    throw new Error("PR_FILES_JSON entries must include filename, additions and deletions.");
  }
  return files;
}

async function fetchPrFiles() {
  if (process.env.PR_FILES_JSON) return parseFilesJson(process.env.PR_FILES_JSON);

  const token = process.env.GITHUB_TOKEN;
  const repo = process.env.GITHUB_REPOSITORY;
  const prNumber = process.env.PR_NUMBER;
  if (!token || !repo || !prNumber) return null;

  const gh = ghClient(token);
  const files = (await paginate(gh, `/repos/${repo}/pulls/${prNumber}/files`)).map(parseFileEntry);
  if (files.some((file) => !file)) throw new Error("GitHub PR file payload was missing filename/additions/deletions.");
  return files;
}

let size;
try {
  const files = await fetchPrFiles();
  if (files) {
    const included = [];
    const excluded = [];
    for (const file of files) {
      const pattern = excludedBy(file.filename);
      if (pattern) {
        excluded.push({ ...file, pattern });
      } else {
        included.push(file);
      }
    }
    size = {
      changedLines: included.reduce((sum, file) => sum + file.additions + file.deletions, 0),
      changedFiles: included.length,
      excluded,
      usedFileDetails: true,
    };
  }
} catch (e) {
  // A transient failure reading the per-file listing (a GitHub API hiccup) must
  // not red-gate a legitimate PR: the pull_request event payload's aggregate
  // counts are wired into env as the designed fallback. Degrade to those
  // aggregates — path exclusions can't be applied without the file list, and
  // the output says so — and only hard-fail (Hard Rule 8) when neither the file
  // details nor the aggregate counts are usable.
  if (additions !== null && deletions !== null && changedFiles !== null) {
    const msg = `PR size check could not read PR file details (${e.message}); falling back to the event payload's aggregate counts. Path exclusions were not applied.`;
    console.log(`::warning::${msg}`);
    summary(`### PR size\n\n> ⚠️ ${msg}\n`);
    console.warn(msg);
    size = { changedLines: additions + deletions, changedFiles, excluded: [], usedFileDetails: false };
  } else {
    const msg = `PR size check could not read PR file details and no aggregate counts were available to fall back on: ${e.message}`;
    errorAnnot(msg);
    summary(`### PR size\n\n❌ ${msg}`);
    console.error(msg);
    process.exit(1);
  }
}

if (!size) {
  if (additions === null || deletions === null || changedFiles === null) {
    const msg =
      "PR size check could not determine the PR's size: expected PR file details " +
      "(PR_FILES_JSON or GITHUB_TOKEN/GITHUB_REPOSITORY/PR_NUMBER) or numeric " +
      "PR_ADDITIONS, PR_DELETIONS and PR_CHANGED_FILES from the pull_request " +
      "event payload. Check the pr-gates workflow wiring.";
    errorAnnot(msg);
    summary(`### PR size\n\n❌ ${msg}`);
    console.error(msg);
    process.exit(1);
  }
  size = { changedLines: additions + deletions, changedFiles, excluded: [], usedFileDetails: false };
}

const changedLines = size.changedLines;
const countedFiles = size.changedFiles;
const exclusionNote = size.usedFileDetails
  ? ` Exclusions applied: ${excludePatterns.join(", ")}. Excluded ${size.excluded.length} file(s).`
  : " Path exclusions were not applied because PR file details were unavailable.";
const overLines = changedLines > maxLines;
const overFiles = countedFiles > maxFiles;

if (!overLines && !overFiles) {
  const msg = `PR size OK: ${changedLines} changed line(s) ≤ ${maxLines}, ${countedFiles} file(s) ≤ ${maxFiles}.${exclusionNote}`;
  notice(msg);
  summary(`### PR size\n\n✅ ${msg}`);
  console.log(msg);
  process.exit(0);
}

// --- over the limit: fail with the numbers and the protocol (criterion 2) ---
const breaches = [
  overLines ? `${changedLines} changed lines (limit ${maxLines})` : null,
  overFiles ? `${countedFiles} files (limit ${maxFiles})` : null,
].filter(Boolean);

const message = [
  `PR size limit exceeded: ${breaches.join(" and ")}.`,
  "",
  "Oversized PRs are the biggest drain on review quality, and review is the",
  "loop's bottleneck. Per AGENTS.md step 3, when scope exceeds the issue",
  `(~${maxLines} changed lines or ~${maxFiles} files), split and requeue:`,
  "  1. comment a proposed split on the issue,",
  "  2. reset it to state:ready and remove state:in-progress,",
  "  3. exit — the split becomes new plan/*.md files or issues.",
  "",
  `Thresholds are configured in ${GATES_FILE} (max_changed_lines / max_changed_files).`,
  `Exclusions applied: ${excludePatterns.join(", ")}.`,
  `Excluded file(s): ${size.excluded.length ? size.excluded.map((file) => `${file.filename} (${file.pattern})`).join(", ") : "none"}.`,
].join("\n");

errorAnnot(`PR size limit exceeded: ${breaches.join("; ")}.`);
summary(`### PR size\n\n❌ ${message.replace(/\n/g, "\n")}`);
console.error(message);
process.exit(1);
